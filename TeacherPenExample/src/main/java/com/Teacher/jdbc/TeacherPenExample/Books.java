package com.Teacher.jdbc.TeacherPenExample;

public class Books {
	private String bname;
	private int nop;
	
	public Books()
	{
		
	}

	public String getBname() {
		return bname;
	}

	public void setBname(String bname) {
		this.bname = bname;
	}

	public int getNop() {
		return nop;
	}

	public void setNop(int nop) {
		this.nop = nop;
	}

}
